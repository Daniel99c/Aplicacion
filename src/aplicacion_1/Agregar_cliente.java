    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Resultado.setText(numero.getText());
        if (!validarNumeros(numero.getText().trim()))
       {
           JOptionPane.showMessageDialog(rootPane, "El Dato que ingreso es invalido");
           numero.setText("");
       }
       else 
        JOptionPane.showMessageDialog(rootPane, "El Dato que ingreso es valido");
       numeros [indice]=Integer.parseInt(numero.getText());
       indice++;
       numero.setText("");    
    }//GEN-LAST:event_jButton1ActionPerformed
public static boolean validarNumeros(String datos)
    {
        return datos.matches("[0-9]*");
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String mensaje=" ";
        for (int i=0; i<indice; i++)
        {
            mensaje=mensaje+numeros[i]+"-";  
        }
        Resultado.setText(mensaje);
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
     for(int i=0; i < (indice-1)  ;i ++)
          {
              for (int j=0;j<(indice-1);j++) 
              {
                  if (numeros[j]> numeros [j+1])
                  {
                     int aux= numeros[j];
                      numeros[j]=numeros[j+1];
                      numeros[j+1]= aux;
                  }
              }
          }
        // Mostrar la lista ordenada 
        String mensajeLista = " ";
        for (int i=0;i<indice;i++)
        {
         mensajeLista = mensajeLista + numeros [i]+"-";
        Resultado.setText(mensajeLista);
        }    
    }//GEN-LAST:event_jButton3ActionPerformed

